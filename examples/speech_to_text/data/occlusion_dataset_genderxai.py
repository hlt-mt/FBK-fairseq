from typing import List, Tuple, Dict, Optional, Union
import logging

import numpy as np
import torch
from torch import Tensor

from examples.speech_to_text.data.occlusion_dataset import OccludedSpeechToTextDataset
from examples.speech_to_text.occlusion_explanation.perturbators import OcclusionFbankPerturbator
from fairseq.data import ConcatDataset, Dictionary
from fairseq.data.audio.speech_to_text_dataset import _collate_frames, SpeechToTextDataset
from fairseq.data import data_utils


LOGGER = logging.getLogger(__name__)


class OccludedSpeechToTextDatasetGenderXai(OccludedSpeechToTextDataset):
    """
    Adapts the OccludedSpeechToTextDataset class so samples contain information about the position 
    and correct and incorrect forms of gender terms as well as a version of the target text with 
    the gender swapped.
    """

    def __getitem__(self, perturb_index: int) -> Tuple[
            int, int, Tensor, Tensor, Tensor, List[str], List[str], List[int], List[str], List[str], Optional[Tensor]]:
        
        orig_dataset_index = self._original_index(perturb_index)
        returned_tuple = self.dataset[orig_dataset_index]
        
        if len(returned_tuple) == 7:  # If the dataset does not contain source texts
            index, fbank, tgt_tokens, found_terms, found_term_pairs, \
                gender_terms_indices, swapped_tgt_tokens = returned_tuple
            source_text = None
        else:
            index, fbank, tgt_tokens, source_text, found_terms, found_term_pairs, \
                gender_terms_indices, swapped_tgt_tokens = returned_tuple
        
        mask, perturbed_fbank = self.perturbator(fbank, orig_dataset_index, perturb_index)
        
        return (perturb_index,
                orig_dataset_index,
                mask,
                perturbed_fbank,
                tgt_tokens,
                source_text,
                found_terms,
                found_term_pairs,
                gender_terms_indices,
                swapped_tgt_tokens)


    def collater(self, samples: List[Tuple[int, int, Tensor, Tensor, Tensor, Optional[Tensor]]]) -> Dict:
        if len(samples) == 0:
            return {}
        perturb_indices = torch.tensor([i for i, _, _, _, _, _, _, _, _, _ in samples], dtype=torch.long)
        orig_indices = torch.tensor([i for _, i, _, _, _, _, _, _, _, _ in samples], dtype=torch.long)
        masks = _collate_frames([m for _, _, m, _, _, _, _, _, _, _ in samples])
        frames = _collate_frames([s for _, _, _, s, _, _, _, _, _, _ in samples])
        n_frames = torch.tensor([s.size(0) for _, _, _, s, _, _, _, _, _, _ in samples], dtype=torch.long)
        n_frames, order = n_frames.sort(descending=True)
        perturb_indices = perturb_indices.index_select(0, order)
        orig_indices = orig_indices.index_select(0, order)
        frames = frames.index_select(0, order)
        masks = masks.index_select(0, order)

        # In this class, the target corresponds to the hypothesis previously generated by the model,
        # not to the reference translation/transcript.
        target = data_utils.collate_tokens(
            [p for _, _, _, _, p, _, _, _, _, _ in samples],
            self.tgt_dict.pad(),
            self.tgt_dict.eos(),
            left_pad=False,
            move_eos_to_beginning=False,
        )
        target = target.index_select(0, order)
        target_lengths = torch.tensor(
            [p.size(0) for _, _, _, _, p, _, _, _, _, _ in samples], dtype=torch.long
        ).index_select(0, order)

        # In this class, the previous output tokens correspond to the hypothesis previously generated by the model
        prev_output_tokens = data_utils.collate_tokens(
            [p for _, _, _, _, p, _, _, _, _, _ in samples],
            self.tgt_dict.pad(),
            self.tgt_dict.eos(),
            left_pad=False,
            move_eos_to_beginning=True,
        )
        prev_output_tokens = prev_output_tokens.index_select(0, order)

        src_texts = [s for _, _, _, _, _, s, _, _, _, _ in samples]
        src_texts = [src_texts[i] for i in order]
        found_terms = [ft for _, _, _, _, _, _, ft, _, _, _ in samples]
        found_terms = [found_terms[i] for i in order]
        found_term_pairs = [ftp for _, _, _, _, _, _, _, ftp, _, _ in samples]
        found_term_pairs = [found_term_pairs[i] for i in order]
        gender_terms_indices = [gti for _, _, _, _, _, _, _, _, gti, _ in samples]
        gender_terms_indices = [gender_terms_indices[i] for i in order]

        swapped_target = data_utils.collate_tokens(
            [st for _, _, _, _, _, _, _, _, _, st in samples],
            self.tgt_dict.pad(),
            self.tgt_dict.eos(),
            left_pad=False,
            move_eos_to_beginning=False,
        )
        swapped_target = swapped_target.index_select(0, order)
        swapped_target_lengths = torch.tensor(
            [st.size(0) for _, _, _, _, _, _, _, _, _, st in samples],
            dtype=torch.long
        ).index_select(0, order)
        

        out = {
            "id": perturb_indices,
            "orig_id": orig_indices,
            "net_input": {
                "src_tokens": frames,
                "src_lengths": n_frames,
                "prev_output_tokens": prev_output_tokens},
            "target": target,
            "target_lengths": target_lengths,
            "masks": masks,
            "src_texts": src_texts,
            "nsentences": len(samples),
            "found_terms": found_terms,
            "found_term_pairs": found_term_pairs,
            "gender_terms_indices": gender_terms_indices,
            "swapped_target": swapped_target,
            "swapped_target_lengths": swapped_target_lengths}
        return out
